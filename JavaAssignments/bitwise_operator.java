package JavaAssignments;

public class bitwise_operator {

	public static void main(String[] args) {
		
		int a=5;
		int b=7;
		
		System.out.println("a&b is " + (a&b));
		System.out.println("a|b is " + (a|b));
		System.out.println("a^b is " + (a^b));
		System.out.println("~a is " + ~a);
	}

}
